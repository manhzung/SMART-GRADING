export interface ExamVersion {
  _id: string;
  examId: string;
  versionCode: string;
  numberOfQuestions: number;
  questionIds: string[];
  questions?: ExamVersionQuestion[];
  distribution: Record<string, number>;
  submissionCount: number;
  createdAt: string;
  // AMC fields
  paperEngine?: 'amc';
  pdfUrl?: string | null;
  answerSheetPdfUrl?: string | null;
  corrigePdfUrl?: string | null;
  generatedAt?: string | null;
  generationErrors?: string[];
  // Note: templateJson is NOT stored on ExamVersion anymore.
  // It lives on OMRTemplate.templateJson (returned via /exams/:id/template)
}

export interface ExamTemplateData {
  examId: string;
  versionCode: string;
  template: OMRTemplateJson | null;
  answerKey: Record<string, string>;
  totalScore: number;
  numberOfQuestions: number;
}

/**
 * OMRTemplate.templateJson — AMC-generated per-bubble coordinates
 * Used by mobile engine_v2 for OMR scanning.
 * Generated by: AMC → .calage.xy → buildTemplate() → OMRTemplate.templateJson
 */
export interface OMRTemplateJson {
  examId: string;
  title: string;
  paperSize: string;
  scanDpi: number;
  scale: number;
  pageWidth: number;
  pageHeight: number;
  bubbleWidth: number;
  bubbleHeight: number;
  /** Số báo danh bubble coords */
  studentId: { digits: number; coords: Array<{ x: number; y: number; w: number; h: number }> };
  /** Mã đề bubble coords */
  versionCodeZone: { digits: number; coords: Array<{ x: number; y: number; w: number; h: number }> };
  /** Bubble coords per question: answers["q1"] = { "A": {x,y,w,h}, "B": {...}, ... } */
  answers: Record<string, Record<string, { x: number; y: number; w: number; h: number }>>;
  /** Correct answers: answerKey["q1"] = "A" */
  answerKey: Record<string, string>;
  /** Scores per question: questionScores["q1"] = 0.5 */
  questionScores: Record<string, number>;
  totalScore: number;
  numberOfQuestions: number;
  autoAlign: boolean;
  generatedAt: string;
  source: string;
}

